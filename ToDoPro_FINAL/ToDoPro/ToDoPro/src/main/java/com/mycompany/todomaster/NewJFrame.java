    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (todoManager.saveTasksToFile(this)) {
            showStatus("Tasks saved successfully.");
        } else {
            showStatus("Task save canceled or failed.");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadActionPerformed
        // TODO add your handling code here:
        if (todoManager.loadTasksFromFile(this)) {
            showStatus("Tasks loaded successfully. Total: " + todoManager.getTaskCount());
        } else {
            showStatus("Task load canceled or failed.");
        }
    }//GEN-LAST:event_btnLoadActionPerformed

    private void btnExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExitActionPerformed
        // TODO add your handling code here:
        if (todoManager.getTaskCount() > 0) {
            int response = JOptionPane.showConfirmDialog(this, 
                "Do you want to save your tasks before exiting?", 
                "Exit Confirmation", 
                JOptionPane.YES_NO_CANCEL_OPTION);
            
            if (response == JOptionPane.YES_OPTION && todoManager.saveTasksToFile(this)) {
                System.exit(0);
            } else if (response == JOptionPane.NO_OPTION) {
                System.exit(0);
            }
        } else {
            System.exit(0);
        }
    }//GEN-LAST:event_btnExitActionPerformed

    private void btnClearCompletedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearCompletedActionPerformed
        // TODO add your handling code here:
        int count = todoManager.clearCompletedTasks();
        showStatus(count > 0 ? count + " completed task(s) removed." : "No completed tasks to remove.");
    }//GEN-LAST:event_btnClearCompletedActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedIndex = jList1.getSelectedIndex();
        if (selectedIndex == -1) {
            showStatus("Please select a task to delete.");
            return;
        }
        
        if (JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to delete this task?", 
            "Confirm Delete", 
            JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            
            if (todoManager.deleteTask(selectedIndex)) {
                showStatus("Task deleted successfully.");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
    private void addTask() {
        String taskDescription = txtInput.getText().trim();
        if (taskDescription.isEmpty()) {
            showStatus("Please enter a task description.");
            return;
        }
        
        if (todoManager.addTask(taskDescription)) {
            txtInput.setText("");
            showStatus("Task added successfully.");
        }
    }
    private void showStatus(String message) {
        lblStatus.setText(message);
        new javax.swing.Timer(50000, e -> lblStatus.setText("")).start();
    }
    
