    private void btnMarkDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMarkDoneActionPerformed
        // TODO add your handling code here:
        int selectedIndex = jList1.getSelectedIndex();
        if (selectedIndex == -1) {
            showStatus("Please select a task to mark as done.");
            return;
        }
        if (todoManager.markTaskAsCompleted(selectedIndex)) {
            showStatus("Task marked as completed.");
        } else {
            showStatus("Failed to mark task as completed.");
        }
    }//GEN-LAST:event_btnMarkDoneActionPerformed

